// Tipos de dados
// Numericos (Inteiros e Reais)
public class Programa02 {

    public static void main(String[] args) {
        // Tipos primeiros/primitivos
        // Por padrao, os numeros reais em Java sao considerados double
        float preco1 = 23.4f; // 23.40
        double preco2 = 23.4; // 23.43434343434343434343

        // tipos nao primarios/primitivos
        Float preco3 = 44.5f;
        Double preco4 = 44.5;

        System.out.println("float -> preco1: " + preco1);
        System.out.println("double -> preco2: " + preco2);
        System.out.println("Float -> preco3: " + preco3);
        System.out.println("Double -> preco4: " + preco4);
    }
}
